		<h1 class="entry-title">10 Tips Cara Merawat Barang&nbsp;Elektronik</h1>

				<div class="entry-meta">
						<span class="sep">Ditulis pada </span><a href="https://tokoelektronikdijawa.wordpress.com/2016/05/29/10-tips-cara-merawat-barang-elektronik/" title="5:09 am" rel="bookmark"><time class="entry-date" datetime="2016-05-29T05:09:32+00:00">Mei 29, 2016</time></a><span class="by-author"> <span class="sep"> oleh </span> <span class="author vcard"><a class="url fn n" href="https://tokoelektronikdijawa.wordpress.com/author/tokoelektronikdijawa/" title="Lihat semua tulisan milik tokoelektronikdijawa" rel="author">tokoelektronikdijawa</a></span></span>		</div><!-- .entry-meta -->
