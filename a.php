		<p><img loading="lazy" data-attachment-id="396" data-permalink="https://tokoelektronikdijawa.wordpress.com/2016/05/29/10-tips-cara-merawat-barang-elektronik/download/" data-orig-file="https://tokoelektronikdijawa.files.wordpress.com/2016/05/download.jpg" data-orig-size="259,194" data-comments-opened="1" data-image-meta="{&quot;aperture&quot;:&quot;0&quot;,&quot;credit&quot;:&quot;&quot;,&quot;camera&quot;:&quot;&quot;,&quot;caption&quot;:&quot;&quot;,&quot;created_timestamp&quot;:&quot;0&quot;,&quot;copyright&quot;:&quot;&quot;,&quot;focal_length&quot;:&quot;0&quot;,&quot;iso&quot;:&quot;0&quot;,&quot;shutter_speed&quot;:&quot;0&quot;,&quot;title&quot;:&quot;&quot;,&quot;orientation&quot;:&quot;0&quot;}" data-image-title="download" data-image-description="" data-medium-file="https://tokoelektronikdijawa.files.wordpress.com/2016/05/download.jpg?w=259" data-large-file="https://tokoelektronikdijawa.files.wordpress.com/2016/05/download.jpg?w=259" class="wp-image-396 aligncenter" src="https://tokoelektronikdijawa.files.wordpress.com/2016/05/download.jpg?w=372&#038;h=283" alt="download" width="372" height="283" /></p>
<p style="text-align:center;">Barang elektronik dapat dijumpai dimana-mana. Kemana pun kita pergi selalu saja ada orang yang menggunakan handphone, notebook, MP3 player, dan lain-lain. Bahkan bisa saja, seseorang memiliki barang elektronik lebih dari satu. Fungsi barang elektronik sangat beragam, bahkan mungkin masih dapat berkembang. Misalnya, aplikasi telepon berkembang menjadi aplikasi pengiriman data teks dan gambar. Ditambah lagi dengan terintegrasinya jaringan internet dengan barang-barang elektronik. Melihat perkembangan barang elektronik saat ini maka kita harus pAndai-pAndai merawat dan menjaga barang elektronik. Di posting kali ini, akan saya berikan beberapa saran untuk merawat barang elektronik Anda.</p>
<ol style="text-align:center;">
<li>Berikan pembungkus atau wadah sehingga tidak terkena debu. Selain itu juga menghindarkan dari benturan langsung. Wadah tersebut juga harus memiliki lubang agar panas dari barang elektronik dapat keluar.</li>
<li>Gunakan lap dari bahan yang halus seperti lap kacamata untuk mengelap baik layar atau pun bagian lain.</li>
<li>Gunakan cairan pembersih khusus/lens cleaner untuk membersihkan noda yang menempel pada layar.</li>
<li>Berikan waktu yang cukup bagi barang elektronik untuk menghilangkan panasnya setelah dipakai dalam waktu yang cukup lama.</li>
<li>Simpan barang elektronik di tempat yang cukup dingin namun tidak lembab karena kelembaban dapat mempercepat kerusakan barang elektronik.</li>
<li>Hindarkan dari kontak matahari secara langsung dan ruangan-ruangan panas. Panas yang berlebihan dapat merusak komponen-komponen yang ada. Contoh ruangan panas adalah ruangan mobil yang diparkir di bawah sinar matahari.</li>
<li>Jika tidak mengerti bagaimana memperbaiki barang elektronik, bawalah ke tempat servis yang benar-benar bisa dipercaya, atau pusat servis produk.</li>
<li>Simpan kartu garansi barang elektronik Anda. Umumnya, Anda memperoleh kesempatan servis gratis atau pemeliharaan produk. Ini cara hemat untuk tetap bisa memakai barang elektronik Anda.</li>
<li>Hindari memindahkan barang elektronik berukuran sedang atau besar dalam kondisi menyala. Sebaiknya, masukkan ke dalam mode standby atau hibernate (jika ada).</li>
<li>Jauhkan barang elektronik dari makanan dan minuman. Siapa yang tahu kapan makanan atau minuman dapat mengotori bahkan merusak barang elektronik Anda.</li>
</ol>
<p style="text-align:center;">Dengan perawatan yang maksimal tentu kinerja barang elektronik kita juga akan optimal sehingga akan makin mempermudah aktivitas kita dengan fungsi-fungsinya yang beragam.</p>
